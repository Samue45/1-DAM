package Theme1;

public class Operations {

    public static int sum(int a, int b){
        return a+b;
    }

    public static int rest(int a, int b){
        return a-b;
    }

    public static int multi(int a, int b){
        return a*b;
    }

    public static int div(int a, int b){
        return a/b;
    }


}
